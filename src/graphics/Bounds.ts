/**
 * Coordinates of a box shape.
 */
export interface Bounds {
    /**
     * X-axis coordinate of the box's starting point.
     */
    x: number;
    /**
     * Y-axis coordinate of the box's starting point.
     */
    y: number;
    /**
     * The box's width.
     */
    width: number;
    /**
     * The box's height.
     */
    height: number;
}
